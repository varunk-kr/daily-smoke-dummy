    public static HashMap<String, String> getTestDataMap(String scenario, String SheetName) {
        HashMap<String, String> readMap = new HashMap<>();
        try {
            FileInputStream fileInput = new FileInputStream(TEST_DATA_PATH);
            Workbook workbook = WorkbookFactory.create(fileInput);
            Sheet sheet = workbook.getSheet(SheetName);
            int rowCount = 0;
            int rowHeaderIndex = 0;
            List<String> headerList = new ArrayList<>();
            Row headerRow = sheet.getRow(rowHeaderIndex);
            for (int i = 0; i < headerRow.getLastCellNum(); i++) {
                headerList.add(i, sheet.getRow(rowHeaderIndex).getCell(i).toString());
            }
            int scenarioColumnIndex = headerList.indexOf(SCENARIO);
            for (Row row : sheet) {
                if (row.getCell(scenarioColumnIndex).toString().equalsIgnoreCase(scenario)) {
                    break;
                }
                rowCount = rowCount + 1;
            }
            int columnCount = sheet.getRow(rowCount).getLastCellNum();
            for (int i = 0; i < columnCount; i++) {
                if (sheet.getRow(rowCount).getCell(i) != null) {
                    readMap.put(sheet.getRow(rowHeaderIndex).getCell(i).toString(), sheet.getRow(rowCount).getCell(i).toString());
                }
            }
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
        return readMap;
    }

