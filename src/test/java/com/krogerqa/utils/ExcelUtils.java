    public static void writeToExcel(String sheetName, HashMap<String, String> outputDataMap) {
        String filePath = "./src/test/resources/input-data/TestData.xlsx";
        try {
            Workbook workbook = new XSSFWorkbook(Files.newInputStream(Paths.get(filePath)));
            Sheet sheet = getSheet(sheetName, workbook);
            AtomicInteger columnCount = new AtomicInteger();
            sheet.createRow(0);
            sheet.createRow(1);
            outputDataMap.forEach((key, value) -> {
                sheet.getRow(0).createCell(columnCount.get()).setCellValue(key);
                sheet.getRow(1).createCell(columnCount.get()).setCellValue(value);
                columnCount.set(columnCount.get() + 1);
            });
            FileOutputStream outputStream = new FileOutputStream(filePath);
            workbook.write(outputStream);
            workbook.close();
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static void removeExistingDataFromExcel(String sheetName) {
        try {
            String filePath = "./src/test/resources/input-data/TestData.xlsx";
            Workbook workbook = new XSSFWorkbook(Files.newInputStream(Paths.get(filePath)));
            int index = workbook.getSheetIndex(sheetName);
            if (index > 1) {
                workbook.removeSheetAt(index);
                FileOutputStream fileOutputStream = new FileOutputStream(filePath);
                workbook.write(fileOutputStream);
                fileOutputStream.close();
            }
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    private static Sheet getSheet(String sheetName, Workbook workbook) {
        List<String> sheetNames = new ArrayList<>(30);
        for (int i = 0; i < workbook.getNumberOfSheets(); i++) {
            sheetNames.add(workbook.getSheetName(i));
        }
        if (!sheetNames.contains(sheetName)) {
            workbook.createSheet(sheetName);
        }
        return workbook.getSheet(sheetName);
    }

    public static HashMap<String, String> convertStringToMap(String value) {
        HashMap<String, String> convertedMap = new HashMap<>();
        value = value.substring(1, value.length() - 1);
        String[] keyValuePairs = value.split(",");
        for (String pair : keyValuePairs) {
            String[] keyValue = pair.trim().split("=");
            convertedMap.put(keyValue[0], keyValue[1]);
        }
        return convertedMap;
    }

    public static List<String> convertStringToList(String value) {
        value = value.substring(1, value.length() - 1);
        value = value.replace(" ", "");
        return Arrays.asList(value.split(",", -1));
    }

    public static String getKeyValue(HashMap<String, String> testDataMap, String key) {
        String mapValue;
        try {
            mapValue = testDataMap.get(key);
        } catch (Exception e) {
            throw new RuntimeException("Unable to retrieve value for " + key + e);
        }
        return mapValue;
    }

